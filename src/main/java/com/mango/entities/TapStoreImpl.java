package com.mango.entities;



import javax.persistence.Id;

import org.springframework.data.mongodb.core.mapping.Document;

@Document(collection = "store")
public class TapStoreImpl implements TapStore {
	@Id
	private String id;

	private String name;

	private String address1;

	private String address2;

	private String city;

	private String state;

	private String zip;

	private String country;

	private String phone;

	private Double latitude;

	private Double longitude;

	private boolean isStoreActive;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddress1() {
		return address1;
	}

	public void setAddress1(String address1) {
		this.address1 = address1;
	}

	public String getAddress2() {
		return address2;
	}

	public void setAddress2(String address2) {
		this.address2 = address2;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getZip() {
		return zip;
	}

	public void setZip(String zip) {
		this.zip = zip;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public Double getLatitude() {
		return latitude;
	}

	public void setLatitude(Double latitude) {
		this.latitude = latitude;
	}

	public Double getLongitude() {
		return longitude;
	}

	public void setLongitude(Double longitude) {
		this.longitude = longitude;
	}

	public boolean isStoreActive() {
		return isStoreActive;
	}

	public void setStoreActive(boolean isStoreActive) {
		this.isStoreActive = isStoreActive;
	}

}
