public class TapDeptDimensionImpl implements TapDeptDimension {

	protected Long id;

	protected String dimensiontype;

	protected String dimensionshape;

	protected String dimensionPositionX;

	protected String dimensionPositionY;

	protected String dimensionWidth;

	protected String dimensionLength;

	protected String dimensionRadius;

	protected String dimensionHeight;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getDimensiontype() {
		return dimensiontype;
	}

	public void setDimensiontype(String dimensiontype) {
		this.dimensiontype = dimensiontype;
	}

	public String getDimensionshape() {
		return dimensionshape;
	}

	public void setDimensionshape(String dimensionshape) {
		this.dimensionshape = dimensionshape;
	}

	public String getDimensionPositionX() {
		return dimensionPositionX;
	}

	public void setDimensionPositionX(String dimensionPositionX) {
		this.dimensionPositionX = dimensionPositionX;
	}

	public String getDimensionPositionY() {
		return dimensionPositionY;
	}

	public void setDimensionPositionY(String dimensionPositionY) {
		this.dimensionPositionY = dimensionPositionY;
	}

	public String getDimensionWidth() {
		return dimensionWidth;
	}

	public void setDimensionWidth(String dimensionWidth) {
		this.dimensionWidth = dimensionWidth;
	}

	public String getDimensionLength() {
		return dimensionLength;
	}

	public void setDimensionLength(String dimensionLength) {
		this.dimensionLength = dimensionLength;
	}

	public String getDimensionRadius() {
		return dimensionRadius;
	}

	public void setDimensionRadius(String dimensionRadius) {
		this.dimensionRadius = dimensionRadius;
	}

	public String getDimensionHeight() {
		return dimensionHeight;
	}

	public void setDimensionHeight(String dimensionHeight) {
		this.dimensionHeight = dimensionHeight;
	}


	

}
